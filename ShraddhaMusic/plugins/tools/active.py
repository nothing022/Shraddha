from ShraddhaMusic import app
from ShraddhaMusic.misc import SUDOERS
from ShraddhaMusic.utils.inline import close_markup
from ShraddhaMusic.utils.database import (
    get_active_chats,
    get_active_video_chats,
    remove_active_chat,
    remove_active_video_chat,
)


@app.on_message(filters.command(["activevc", "activevoice"]) & SUDOERS)
async def activevccmd(client,message):
   inline = False
   await activevc(client,message,inline)

@app.on_callback_query(filters.regex("refreshactivevc") & ~ SUDOERS)
async def sysinfocmd(client, update):
   await update.answer("❌ ONLY SUDO USERS ALLOWED TO REFRESH STATS❗", show_alert=True)
   
@app.on_callback_query(filters.regex("refreshactivevc") & SUDOERS)
async def sysinfocmd(client, update):
   inline = True
   message = update.message
   await activevc(client,message,inline)
   await update.answer("🖥️ Chatlist updated❗")

async def activevc(client,message,inline):
    keyboard = InlineKeyboardMarkup([[InlineKeyboardButton("⍟ ʀᴇꜰʀᴇꜱʜ ⍟",callback_data="refreshactivevc")],[InlineKeyboardButton("⍟ ᴄʟᴏꜱᴇ ⍟",callback_data="close")]])
    if inline:
      mystic = await message.edit_text("» ɢᴇᴛᴛɪɴɢ ᴀᴄᴛɪᴠᴇ ᴠᴏɪᴄᴇ ᴄʜᴀᴛs ʟɪsᴛ...", reply_markup=keyboard)
    else:
      mystic = await message.reply_text("» ɢᴇᴛᴛɪɴɢ ᴀᴄᴛɪᴠᴇ ᴠᴏɪᴄᴇ ᴄʜᴀᴛs ʟɪsᴛ...", reply_markup=keyboard)
    served_chats = await get_active_chats()
    text = ""
    j = 0
    for x in served_chats:
        try:
            title = (await app.get_chat(x)).title
        except Exception as e:
            print(e)
            title = "Please Retry [Telegram Issue]"
            #await remove_active_chat(x)
            continue
        try:
            if (await app.get_chat(x)).username:
                user = (await app.get_chat(x)).username
                text += f"<b>{j + 1}.</b> <a href=https://t.me/{user}>{unidecode(title).upper()}</a> [<code>{x}</code>]\n"
            else:
                text += (
                    f"<b>{j + 1}.</b> {unidecode(title).upper()} [<code>{x}</code>]\n"
                )
            j += 1
        except:
            continue
    if not text:
        await mystic.edit_text(f"» ɴᴏ ᴀᴄᴛɪᴠᴇ ᴠᴏɪᴄᴇ ᴄʜᴀᴛs ᴏɴ {app.mention}.",reply_markup=keyboard)
    else:
        await mystic.edit_text(
            f"<b>» ʟɪsᴛ ᴏғ ᴄᴜʀʀᴇɴᴛʟʏ ᴀᴄᴛɪᴠᴇ ᴠᴏɪᴄᴇ ᴄʜᴀᴛs :</b>\n\n{text}",
            disable_web_page_preview=True,reply_markup=keyboard)


